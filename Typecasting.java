class Typecasting{
    public static void main(String args[]){

        // Type Casting --> 1. Automated TypeCasting  2. Forcefully By Programmer.
        int i = 5;
        double d = i;
        System.out.println(d);

        // Forcefully Typed Casting.

        double myval = 12.01;
        int myint = (int)myval;
        System.out.println(myint);
    }
}